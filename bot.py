def get_schedule(group_name: str) -> str:
    """–ü–∞—Ä—Å–∏—Ç —Ä–∞—Å–ø–∏—Å–∞–Ω–∏–µ —Å —Å–∞–π—Ç–∞ –¥–ª—è —É–∫–∞–∑–∞–Ω–Ω–æ–π –≥—Ä—É–ø–ø—ã."""
    url = "https://edu.tgpi.ru/schedule/"  # –û–±—Ä–∞—Ç–∏—Ç–µ –≤–Ω–∏–º–∞–Ω–∏–µ: –ø—Ä–æ—Ç–æ–∫–æ–ª –¥–æ–ª–∂–µ–Ω –±—ã—Ç—å https
    
    try:
        response = requests.get(url, timeout=10)
        response.raise_for_status()
    except requests.RequestException as e:
        return f"–û—à–∏–±–∫–∞ –ø—Ä–∏ –∑–∞–ø—Ä–æ—Å–µ –∫ —Å–∞–π—Ç—É: {e}"
    
    soup = BeautifulSoup(response.text, 'html.parser')
    
    # –ü—Ä–∏–º–µ—Ä –ø–æ–∏—Å–∫–∞ –≥—Ä—É–ø–ø—ã ‚Äî –Ω—É–∂–Ω–æ –∞–¥–∞–ø—Ç–∏—Ä–æ–≤–∞—Ç—å –ø–æ–¥ —Ä–µ–∞–ª—å–Ω—É—é —Å—Ç—Ä—É–∫—Ç—É—Ä—É —Å–∞–π—Ç–∞
    group_element = soup.find('div', {'class': 'group', 'data-name': group_name})
    
    if not group_element:
        return f"‚ùå –ì—Ä—É–ø–ø–∞ '{group_name}' –Ω–µ –Ω–∞–π–¥–µ–Ω–∞ –Ω–∞ —Å–∞–π—Ç–µ. –ü—Ä–æ–≤–µ—Ä—å—Ç–µ –Ω–∞–ø–∏—Å–∞–Ω–∏–µ."
    
    schedule_data = []
    days = group_element.find_all('div', class_='day')
    
    for day in days:
        day_name = day.find('h3').text.strip()
        lessons = day.find_all('div', class_='lesson')
        
        day_schedule = f"\nüìÖ {day_name}:\n"
        for lesson in lessons:
            time = lesson.find('span', class_='time').text.strip()
            subject = lesson.find('span', class_='subject').text.strip()
            room = lesson.find('span', class_='room').text.strip()
            day_schedule += f"  ‚è∞ {time} ‚Äî üìö {subject} (üè´ {room})\n"
        schedule_data.append(day_schedule)
    
    return ''.join(schedule_data) if schedule_data else "–†–∞—Å–ø–∏—Å–∞–Ω–∏–µ –Ω–∞ –Ω–µ–¥–µ–ª—é –Ω–µ –Ω–∞–π–¥–µ–Ω–æ."

